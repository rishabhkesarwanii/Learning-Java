public class varaible {

    public static void main(String[] args) {

        // Boolen
        boolean bFlag1 = true;
        boolean bFlag2 = false;

        // integer
        int iAge = 19;

        // float
        double dDouble = 3.4;
        float fNum = 3.4F;

        double dScientific = 3.445e2; // 3.445*10^2

        // Char
        char cLetter = 'r';

        // string
        String sTr1 = "Rishabh Kesarwani";
        String sTr2 = "Learning Java";

        // Output
        System.out.println(bFlag1);
        System.out.println(bFlag2);
        System.out.println(iAge);
        System.out.println(dDouble);
        System.out.println(fNum);
        System.out.println(dScientific);
        System.out.println(cLetter);
        System.out.println(sTr1);
        System.out.println(sTr2);

    }
}
