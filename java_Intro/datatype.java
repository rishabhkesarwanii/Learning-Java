public class datatype {
    public static void main(String[] args) {

        // boolean type
        boolean bFlag = true;

        // Java byte data type
        byte byRange;
        byRange = 124;

        // Java short data type
        short shTemperature;
        shTemperature = -200;

        // int type
        int iRange = -4250000;

        // Java long data type
        long lRange = -42332200000L;

        // double
        double dNumber = -42.3;

        // Java float data type
        float fNumber = -42.3f;

        // char type
        char cLetter = '\u0051';
        char cLetter1 = '9';

        // String type
        String sName = "Rishabh Kesarwani";

        // Output
        System.out.println(bFlag);
        System.out.println(byRange);
        System.out.println(shTemperature);
        System.out.println(iRange);
        System.out.println(lRange);
        System.out.println(dNumber);
        System.out.println(fNumber);
        System.out.println(cLetter);
        System.out.println(cLetter1);
        System.out.println(sName);
    }
}
